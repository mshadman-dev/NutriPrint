let currentPlan = null;

async function generateMeal() {
  const school   = document.getElementById('mealSchool').value.trim();
  const student  = document.getElementById('mealStudent').value.trim() || 'Student';
  const age      = document.getElementById('mealAge').value;
  const diet     = document.getElementById('mealDiet').value;
  const region   = document.getElementById('mealRegion').value;
  const month    = document.getElementById('mealMonth').value;
  const strategy = document.getElementById('mealStrategy').value;

  if (!school) {
    alert('Please enter school name / ಶಾಲೆಯ ಹೆಸರು ನಮೂದಿಸಿ');
    return;
  }

  // Show loading
  document.getElementById('mealLoading').classList.remove('hidden');
  document.getElementById('mealResult').classList.add('hidden');
  document.getElementById('generateBtn').disabled = true;

  try {
    const res = await fetch('/api/meal/generate', {
      method  : 'POST',
      headers : {'Content-Type':'application/json'},
      body    : JSON.stringify({
        school_name  : school,
        student_name : student,
        age_group    : age,
        diet_pref    : diet,
        region,
        month,
        strategy,
      })
    });

    if (!res.ok) throw new Error('Generation failed');
    const plan = await res.json();
currentPlan = plan;
renderMealPlan(plan);

if (plan.plan_id) {
  loadNutritionGap(plan.plan_id, plan.age_group);
}

  } catch(e) {
    alert('Error generating plan. Please try again.');
  } finally {
    document.getElementById('mealLoading').classList.add('hidden');
    document.getElementById('generateBtn').disabled = false;
  }
}

function renderMealPlan(plan) {
  const container = document.getElementById('mealResult');

  // Build 7-day table
  let rows = '';
  plan.week.forEach(day => {
    rows += `
      <tr class="border-b hover:bg-gray-50">
        <td class="py-3 px-4 font-semibold text-gray-700 whitespace-nowrap">
          <div>${day.day}</div>
          <div class="text-xs text-orange-400 kn">${day.day_kn}</div>
        </td>
        ${['breakfast','lunch','dinner'].map(m => `
        <td class="py-3 px-4">
          <div class="font-medium text-sm text-gray-800">${day[m].name_en}</div>
          <div class="text-xs text-orange-500 kn">${day[m].name_kn}</div>
          <div class="text-xs text-gray-400 mt-1">
            🔥${day[m].calories}cal · ₹${day[m].cost_inr}
          </div>
        </td>`).join('')}
        <td class="py-3 px-4 text-center">
          <button onclick="regenerateDay('${day.day}')"
            class="text-xs text-primary border border-primary px-2 py-1 rounded-lg hover:bg-primary hover:text-white transition">
            🔄
          </button>
        </td>
      </tr>`;
  });

  container.innerHTML = `
    <!-- Nutrition summary -->
    <div class="grid grid-cols-4 gap-3 mb-6">
      ${[
        ['🔥','Avg Cal',plan.avg_daily_cal,'kcal/day'],
        ['💪','Protein',plan.avg_protein_g,'g/day'],
        ['🦴','Calcium',plan.avg_calcium_mg,'mg/day'],
        ['🩸','Iron',plan.avg_iron_mg,'mg/day'],
      ].map(([icon,label,val,unit]) => `
        <div class="bg-green-50 rounded-xl p-3 text-center">
          <div class="text-xl">${icon}</div>
          <div class="font-bold text-gray-800 text-sm">${val}</div>
          <div class="text-xs text-gray-400">${label}<br/>${unit}</div>
        </div>`).join('')}
    </div>

    <!-- Cost badge -->
    <div class="flex items-center justify-between mb-4">
      <span class="bg-green-100 text-green-700 text-xs font-bold px-4 py-1.5 rounded-full">
        ✅ Under ₹150/day · ದಿನಕ್ಕೆ ₹150 ಒಳಗೆ
      </span>
      <span class="text-xs text-gray-400">
        Generated by: ${plan.generated_by === 'groq' ? '🤖 Groq AI' : '📋 Local Engine'}
      </span>
    </div>

    <!-- 7-day table -->
    <div class="overflow-x-auto rounded-2xl border">
      <table class="w-full text-sm">
        <thead class="bg-primary text-white">
          <tr>
            <th class="py-3 px-4 text-left">Day</th>
            <th class="py-3 px-4 text-left">Breakfast</th>
            <th class="py-3 px-4 text-left">Lunch</th>
            <th class="py-3 px-4 text-left">Dinner</th>
            <th class="py-3 px-4 text-center">↺</th>
          </tr>
        </thead>
        <tbody class="bg-white">${rows}</tbody>
      </table>
    </div>

    <!-- Action buttons -->
    <div class="flex flex-col sm:flex-row gap-3 mt-6">
      <button onclick="downloadPDF()"
        class="flex-1 bg-primary text-white font-bold py-3 rounded-xl hover:bg-green-700 transition heading">
        📥 Download PDF
      </button>
      <button onclick="shareWhatsApp()"
        class="flex-1 bg-green-500 text-white font-bold py-3 rounded-xl hover:bg-green-600 transition heading">
        📲 Share via WhatsApp
      </button>
      <button onclick="window.print()"
        class="flex-1 border-2 border-gray-300 text-gray-600 font-bold py-3 rounded-xl hover:bg-gray-50 transition heading">
        🖨️ Print
      </button>
    </div>`;

  container.classList.remove('hidden');
  container.scrollIntoView({behavior:'smooth'});
}

async function regenerateDay(dayName) {
  if (!currentPlan?.plan_id) return;
  const res = await fetch(`/api/meal/${currentPlan.plan_id}/day`, {
    method  : 'PATCH',
    headers : {'Content-Type':'application/json'},
    body    : JSON.stringify({plan_id: currentPlan.plan_id, day_name: dayName})
  });
  if (res.ok) {
    const data = await res.json();
    // Update the day in currentPlan
    const idx = currentPlan.week.findIndex(d => d.day === dayName);
    if (idx !== -1) currentPlan.week[idx] = data.day;
    renderMealPlan(currentPlan);
  }
}

function downloadPDF() {
  if (!currentPlan?.share_token) return;
  window.open(`/poster/${currentPlan.share_token}/pdf`, '_blank');
}

function shareWhatsApp() {
  if (!currentPlan?.share_token) {
    alert('Please generate a plan first');
    return;
  }

  // CRITICAL: use share_token URL, NOT homepage
  const planURL = `${window.location.origin}/plan/${currentPlan.share_token}`;
  const msg     = encodeURIComponent(
    `NutriPrint meal plan for ${currentPlan.student_name}:\n${planURL}\n\nGenerated by NutriPrint — Free school nutrition app 🥗`
  );
  window.open(`https://wa.me/?text=${msg}`, '_blank');
}

// Add at bottom of renderMealPlan()
async function loadNutritionGap(planId, ageGroup) {
  const res  = await fetch(
    `/api/bmi/nutrition-gap?plan_id=${planId}&age_group=${ageGroup}`
  );
  const data = await res.json();

  let rows = '';
  data.gaps.forEach(g => {
    const barW = Math.min(g.percent, 100);
    const barC = g.percent < 60 ? '#EF4444'
               : g.percent < 85 ? '#F97316' : '#10B981';
    rows += `
      <div class="mb-4">
        <div class="flex justify-between text-sm mb-1">
          <span class="font-semibold text-gray-700">
            ${g.nutrient} ${g.status}
          </span>
          <span class="text-gray-500">
            ${g.getting}/${g.needed}${g.unit}
          </span>
        </div>
        <div class="w-full bg-gray-100 rounded-full h-3">
          <div class="h-3 rounded-full transition-all duration-700"
            style="width:${barW}%;background:${barC}">
          </div>
        </div>
        ${g.fix_en ? `
        <div class="mt-1 text-xs text-gray-500">
          💡 Add: ${g.fix_en}
          <span class="text-orange-400 kn ml-1">${g.fix_kn}</span>
        </div>` : ''}
      </div>`;
  });

  // Inject into meal result
  document.getElementById('mealResult').insertAdjacentHTML(
    'afterbegin',
    `<div class="bg-white rounded-2xl border p-6 mb-6">
       <h3 class="heading font-bold text-gray-800 mb-4">
         📊 Nutrition Gap Analysis
         <span class="kn text-orange-400 text-base ml-2">
           ಪೋಷಕಾಂಶ ಕೊರತೆ ವಿಶ್ಲೇಷಣೆ
         </span>
       </h3>
       ${rows}
     </div>`
  );
}
